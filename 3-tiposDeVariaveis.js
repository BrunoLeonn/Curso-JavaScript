// # Tipos de Variáveis e como visualizar

let nome = 'Bruno'; // string literal = nome, com letras, espaço, caracteres especiais, números
let idade = 19; // number literal = qualquer valor, numeros decimais também
let estaAprovado = true; //Boolean = Regras, aprovado ou não, True or False
let sobrenome = undefined ; // Undefined
let corSelecionado = null; // object = Redefinir um valor (botao para alterar cor de um site exemplo)

// Através do typeof, você consegue visualizar o tipo de variável no console
console.log(typeof nome)
console.log(typeof idade)
console.log(typeof estaAprovado)
console.log(typeof sobrenome)
console.log(typeof corSelecionado)